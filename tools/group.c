/*
 * Standalone TLS key-exchange group selftest. Exercises the group subsystem
 * (crypto/ecc.h registry) against published NIST/RFC known-answer vectors,
 * linked against whichever group backend the crypto build selected (generic C
 * or aws-lc s2n-bignum). One "<name>: ok/FAIL" line per case; non-zero exit on
 * failure.
 *
 * Vectors:
 *   X25519    RFC 7748 section 5.2 (scalar mult) and 6.1 (Diffie-Hellman)
 *   secp256r1 RFC 5903 section 8.1 (ECDH P-256)
 *   secp384r1 RFC 5903 section 8.2 (ECDH P-384)
 *
 * Groups are reached through crypto_group_by_id()/->derive()/->keygen(), so
 * this validates registration, dispatch and the backend arithmetic together.
 * A group whose selected build carries no compute backend (identification
 * only) reports "skip" and does not fail.
 */
#include <hpc/compiler.h>
#include <crypto/ecc.h>

#ifdef CONFIG_CC_CLIB
#include <unistd.h>
#else
#include "nolibc.h"
#endif

static unsigned int slen(const char *s)
{
	unsigned int n = 0;
	while (s[n])
		n++;
	return n;
}

static int report(const char *name, int ok)
{
	if (write(1, name, slen(name))) {}
	if (write(1, ok ? ": ok\n" : ": FAIL\n", ok ? 5 : 7)) {}
	return ok ? 0 : 1;
}

static void skip(const char *name)
{
	if (write(1, name, slen(name))) {}
	if (write(1, ": skip\n", 7)) {}
}

static int eq(const u8 *a, const u8 *b, unsigned int n)
{
	for (unsigned int i = 0; i < n; i++)
		if (a[i] != b[i])
			return 0;
	return 1;
}

