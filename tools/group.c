/* derive(local_priv, peer_pub) == expected shared X */
static int test_ecdh(const struct group_algorithm *g, const u8 *priv,
                     const u8 *peer, const u8 *want)
{
	u8 out[48];

	if (g->shared_secret_size > sizeof(out))
		return 0;
	if (g->derive(g, priv, peer, g->public_key_size, out) != 0)
		return 0;
	return eq(out, want, g->shared_secret_size);
}

/* keygen two ephemeral pairs, cross-derive, secrets must agree */
static int test_agreement(const struct group_algorithm *g)
{
	u8 a_priv[48], a_pub[133], b_priv[48], b_pub[133];
	u8 a_ss[48], b_ss[48];

	if (g->private_key_size > sizeof(a_priv) ||
	    g->public_key_size > sizeof(a_pub) ||
	    g->shared_secret_size > sizeof(a_ss))
		return 0;
	if (g->keygen(g, a_priv, a_pub) != 0)
		return 0;
	if (g->keygen(g, b_priv, b_pub) != 0)
		return 0;
	if (g->derive(g, a_priv, b_pub, g->public_key_size, a_ss) != 0)
		return 0;
	if (g->derive(g, b_priv, a_pub, g->public_key_size, b_ss) != 0)
		return 0;
	return eq(a_ss, b_ss, g->shared_secret_size);
}

/* Run the ECDH KATs (both directions) + agreement for a P-curve group. */
static int test_pcurve(unsigned int id, const char *nm,
                       const u8 *i, const u8 *gi, const u8 *r, const u8 *gr,
                       const u8 *ssx)
{
	const struct group_algorithm *g = crypto_group_by_id(id);
	char b1[32], b2[32], b3[32];
	unsigned int k;
	int rc = 0;

	/* build "<nm>-ecdh-i" etc. without libc string helpers */
	for (k = 0; nm[k]; k++) { b1[k] = nm[k]; b2[k] = nm[k]; b3[k] = nm[k]; }
	{ const char *s = "-ecdh-i";  unsigned j = 0; while (s[j]) { b1[k+j] = s[j]; j++; } b1[k+j] = 0; }
	{ const char *s = "-ecdh-r";  unsigned j = 0; while (s[j]) { b2[k+j] = s[j]; j++; } b2[k+j] = 0; }
	{ const char *s = "-agree";   unsigned j = 0; while (s[j]) { b3[k+j] = s[j]; j++; } b3[k+j] = 0; }

	if (!g || !g->derive) { skip(b1); skip(b2); skip(b3); return 0; }

	rc |= report(b1, test_ecdh(g, i, gr, ssx));   /* i * gr = ssx */
	rc |= report(b2, test_ecdh(g, r, gi, ssx));   /* r * gi = ssx */
	if (g->keygen)
		rc |= report(b3, test_agreement(g));
	else
		skip(b3);
	return rc;
}

int
main(int argc, char *argv[])
{
	const struct group_algorithm *x = crypto_group_by_id(GROUP_X25519);
	int rc = 0;

	(void)argc; (void)argv;

	/* X25519 */
	if (x && x->derive) {
		u8 out[32];
		int ok = x->derive(x, x_scalar, x_point, 32, out) == 0 &&
			 eq(out, x_out, 32);
		rc |= report("x25519-derive", ok);
		if (x->keygen)
			rc |= report("x25519-agree", test_agreement(x));
		else
			skip("x25519-agree");
	} else {
		skip("x25519-derive");
		skip("x25519-agree");
	}

	/* NIST P-256 / P-384 (RFC 5903) */
	rc |= test_pcurve(GROUP_SECP256R1, "secp256r1",
			  p256_i, p256_gi, p256_r, p256_gr, p256_ssx);
	rc |= test_pcurve(GROUP_SECP384R1, "secp384r1",
			  p384_i, p384_gi, p384_r, p384_gr, p384_ssx);

	return rc;
}
