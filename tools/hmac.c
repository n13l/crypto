/*
 * Standalone HMAC selftest. Exercises HMAC-SHA1/224/256/384/512 and
 * HMAC-SHA3-256 against RFC 2202 / RFC 4231 (and Python-hashlib cross-checked)
 * test vectors, linked against whichever digest backend the crypto build
 * selected. One "<name>: ok/FAIL" line is printed per case; the exit status is
 * non-zero if any case fails.
 *
 * All cases use RFC 4231 Test Case 2: key = "Jefe",
 * data = "what do ya want for nothing?".
 */
#include <hpc/compiler.h>
#include <crypto/hmac.h>

#ifdef CONFIG_CC_CLIB
#include <unistd.h>
#else
#include "nolibc.h"
#endif

static unsigned int slen(const char *s)
{
	unsigned int n = 0;
	while (s[n])
		n++;
	return n;
}

static int report(const char *name, int ok)
{
	if (write(1, name, slen(name))) {}
	if (write(1, ok ? ": ok\n" : ": FAIL\n", ok ? 5 : 7)) {}
	return ok ? 0 : 1;
}

static int eq(const u8 *a, const u8 *b, unsigned int n)
{
	for (unsigned int i = 0; i < n; i++)
		if (a[i] != b[i])
			return 0;
	return 1;
}

static const u8 hmac_key[4] = { 0x4a, 0x65, 0x66, 0x65 };	/* "Jefe" */
static const u8 hmac_msg[28] = {
	0x77,0x68,0x61,0x74,0x20,0x64,0x6f,0x20,0x79,0x61,0x20,0x77,
	0x61,0x6e,0x74,0x20,0x66,0x6f,0x72,0x20,0x6e,0x6f,0x74,0x68,
	0x69,0x6e,0x67,0x3f };

static int test_hmac_sha1(void)
{
	static const u8 want[20] = {
		0xef,0xfc,0xdf,0x6a,0xe5,0xeb,0x2f,0xa2,0xd2,0x74,0x16,0xd5,
		0xf1,0x84,0xdf,0x9c,0x25,0x9a,0x7c,0x79 };
	u8 mac[20];

	hmac_sha1_160(hmac_key, sizeof(hmac_key), hmac_msg, sizeof(hmac_msg),
		      mac, sizeof(mac));
	return eq(mac, want, sizeof(want));
}

