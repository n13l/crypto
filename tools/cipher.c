/*
 * Standalone cipher selftest. Exercises the AES-128/256 (CBC, GCM) and
 * ChaCha20-Poly1305 free-function API against published NIST / RFC 7539 test
 * vectors, linked against whichever backend the crypto build selected
 * (generic C or the aws-lc accelerated modules). One "<name>: ok/FAIL" line is
 * printed per case; the exit status is non-zero if any case fails.
 */
#include <hpc/compiler.h>
#include <crypto/cipher.h>
#include <crypto/cipher/aes.h>
#include <crypto/cipher/aes/gcm.h>
#include <crypto/cipher/chachapoly.h>

#ifdef CONFIG_CC_CLIB
#include <unistd.h>
#else
#include "nolibc.h"
#endif

static unsigned int slen(const char *s)
{
	unsigned int n = 0;
	while (s[n])
		n++;
	return n;
}

static int report(const char *name, int ok)
{
	if (write(1, name, slen(name))) {}
	if (write(1, ok ? ": ok\n" : ": FAIL\n", ok ? 5 : 7)) {}
	return ok ? 0 : 1;
}

static int eq(const u8 *a, const u8 *b, unsigned int n)
{
	for (unsigned int i = 0; i < n; i++)
		if (a[i] != b[i])
			return 0;
	return 1;
}

/* AES-128-GCM, NIST GCM Test Case 2 (zero key/iv, 16-byte zero PT, no AAD). */
static int test_aes128_gcm(void)
{
	u8 key[16] = {0}, iv[12] = {0}, pt[16] = {0};
	u8 out[32], dec[16];
	static const u8 ct[16] = {
		0x03,0x88,0xda,0xce,0x60,0xb6,0xa3,0x92,
		0xf3,0x28,0xc2,0xb9,0x71,0xb2,0xfe,0x78 };
	static const u8 tag[16] = {
		0xab,0x6e,0x47,0xd4,0x2c,0xec,0x13,0xbd,
		0xf5,0x3a,0x67,0xb2,0x12,0x57,0xbd,0xdf };

	aes_gcm_encrypt(out, pt, 16, key, 16, iv, 12);
	if (!eq(out, ct, 16) || !eq(out + 16, tag, 16))
		return 0;
	if (aes_gcm_decrypt(dec, out, 32, key, 16, iv, 12) != 0 || !eq(dec, pt, 16))
		return 0;
	/* a tampered tag must be rejected */
	out[31] ^= 0x01;
	if (aes_gcm_decrypt(dec, out, 32, key, 16, iv, 12) == 0)
		return 0;
	return 1;
}

/* AES-256-GCM, NIST GCM Test Case 14 (zero key/iv, 16-byte zero PT, no AAD). */
static int test_aes256_gcm(void)
{
	u8 key[32] = {0}, iv[12] = {0}, pt[16] = {0};
	u8 out[32], dec[16];
	static const u8 ct[16] = {
		0xce,0xa7,0x40,0x3d,0x4d,0x60,0x6b,0x6e,
		0x07,0x4e,0xc5,0xd3,0xba,0xf3,0x9d,0x18 };
	static const u8 tag[16] = {
		0xd0,0xd1,0xc8,0xa7,0x99,0x99,0x6b,0xf0,
		0x26,0x5b,0x98,0xb5,0xd4,0x8a,0xb9,0x19 };

	aes_gcm_encrypt(out, pt, 16, key, 32, iv, 12);
	if (!eq(out, ct, 16) || !eq(out + 16, tag, 16))
		return 0;
	if (aes_gcm_decrypt(dec, out, 32, key, 32, iv, 12) != 0 || !eq(dec, pt, 16))
		return 0;
	return 1;
}

/* AES-128-CBC, NIST SP800-38A F.2.1 first block. */
static int test_aes128_cbc(void)
{
	struct aes128_ctx ctx;
	static const u8 key[16] = {
		0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,
		0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c };
	static const u8 iv[16] = {
		0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
		0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f };
	static const u8 pt[16] = {
		0x6b,0xc1,0xbe,0xe2,0x2e,0x40,0x9f,0x96,
		0xe9,0x3d,0x7e,0x11,0x73,0x93,0x17,0x2a };
	static const u8 want[16] = {
		0x76,0x49,0xab,0xac,0x81,0x19,0xb2,0x46,
		0xce,0xe9,0x8e,0x9b,0x12,0xe9,0x19,0x7d };
	u8 buf[16];

	aes_init_keygen_tables();
	for (unsigned int i = 0; i < 16; i++)
		buf[i] = pt[i];
	aes128_cbc_init_ctx_iv(&ctx, key, iv);
	aes128_cbc_encrypt(&ctx, buf, 16);
	if (!eq(buf, want, 16))
		return 0;
	aes128_cbc_init_ctx_iv(&ctx, key, iv);
	aes128_cbc_decrypt(&ctx, buf, 16);
	return eq(buf, pt, 16);
}

/* ChaCha20-Poly1305 AEAD, RFC 7539 section 2.8.2 (with AAD). */
static int test_chacha20_poly1305(void)
{
	struct chachapoly_ctx ctx;
	u8 key[32];
	static const u8 nonce[12] = {
		0x07,0x00,0x00,0x00,0x40,0x41,0x42,0x43,0x44,0x45,0x46,0x47 };
	static const u8 aad[12] = {
		0x50,0x51,0x52,0x53,0xc0,0xc1,0xc2,0xc3,0xc4,0xc5,0xc6,0xc7 };
	static const char pt[] =
		"Ladies and Gentlemen of the class of '99: If I could offer you "
		"only one tip for the future, sunscreen would be it.";
	const int ptlen = 114;
	static const u8 want_ct[114] = {
		0xd3,0x1a,0x8d,0x34,0x64,0x8e,0x60,0xdb,0x7b,0x86,0xaf,0xbc,
		0x53,0xef,0x7e,0xc2,0xa4,0xad,0xed,0x51,0x29,0x6e,0x08,0xfe,
		0xa9,0xe2,0xb5,0xa7,0x36,0xee,0x62,0xd6,0x3d,0xbe,0xa4,0x5e,
		0x8c,0xa9,0x67,0x12,0x82,0xfa,0xfb,0x69,0xda,0x92,0x72,0x8b,
		0x1a,0x71,0xde,0x0a,0x9e,0x06,0x0b,0x29,0x05,0xd6,0xa5,0xb6,
		0x7e,0xcd,0x3b,0x36,0x92,0xdd,0xbd,0x7f,0x2d,0x77,0x8b,0x8c,
		0x98,0x03,0xae,0xe3,0x28,0x09,0x1b,0x58,0xfa,0xb3,0x24,0xe4,
		0xfa,0xd6,0x75,0x94,0x55,0x85,0x80,0x8b,0x48,0x31,0xd7,0xbc,
		0x3f,0xf4,0xde,0xf0,0x8e,0x4b,0x7a,0x9d,0xe5,0x76,0xd2,0x65,
		0x86,0xce,0xc6,0x4b,0x61,0x16 };
	static const u8 want_tag[16] = {
		0x1a,0xe1,0x0b,0x59,0x4f,0x09,0xe2,0x6a,
		0x7e,0x90,0x2e,0xcb,0xd0,0x60,0x06,0x91 };
	u8 ct[130], dec[114], tag[16];

	for (unsigned int i = 0; i < 32; i++)
		key[i] = (u8)(0x80 + i);

	chachapoly_init(&ctx, key, 256);
	chachapoly_crypt(&ctx, nonce, aad, 12, (void *)pt, ptlen, ct, tag, 16, 1);
	if (!eq(ct, want_ct, ptlen) || !eq(tag, want_tag, 16))
		return 0;

	for (unsigned int i = 0; i < 16; i++)
		ct[ptlen + i] = tag[i];
	chachapoly_init(&ctx, key, 256);
	if (chachapoly_crypt(&ctx, nonce, aad, 12, ct, ptlen, dec,
			     ct + ptlen, 16, 0) != 0)
		return 0;
	return eq(dec, (const u8 *)pt, ptlen);
}

int
main(int argc, char *argv[])
{
	int rc = 0;

	(void)argc; (void)argv;
	rc |= report("aes-128-gcm", test_aes128_gcm());
	rc |= report("aes-256-gcm", test_aes256_gcm());
	rc |= report("aes-128-cbc", test_aes128_cbc());
	rc |= report("chacha20-poly1305", test_chacha20_poly1305());
	return rc;
}
